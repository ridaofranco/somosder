// Función para obtener imagen fija de un blog
export function getBlogImageUrl(blogId: string): string {
  return (
    FIXED_BLOG_IMAGES[blogId] ||
    "https://images.unsplash.com/photo-1540575467063-178a50c2df87?w=1600&h=800&fit=crop&crop=center"
  )
}

// Función para obtener imagen fija para BlogCard
export function getBlogCardImageUrl(blogId: string): string {
  const baseUrl = FIXED_BLOG_IMAGES[blogId] || "https://images.unsplash.com/photo-1540575467063-178a50c2df87"
  return `${baseUrl}?w=800&h=600&fit=crop&crop=center`
}

// Función para obtener imagen de blog con cache
export function getUnsplashImageForBlog(blogId: string): string {
  return getBlogImageUrl(blogId)
}

// Función para obtener estadísticas de cache
export function getCacheStats() {
  return {
    totalImages: Object.keys(FIXED_BLOG_IMAGES).length + Object.keys(FIXED_SERVICE_IMAGES).length,
    blogImages: Object.keys(FIXED_BLOG_IMAGES).length,
    serviceImages: Object.keys(FIXED_SERVICE_IMAGES).length,
    lastUpdated: new Date().toISOString(),
  }
}

// Función para limpiar cache expirado (placeholder para compatibilidad)
export function cleanExpiredCache() {
  console.log("Cache cleanup completed - using fixed URLs, no cleanup needed")
  return {
    cleaned: 0,
    remaining: Object.keys(FIXED_BLOG_IMAGES).length + Object.keys(FIXED_SERVICE_IMAGES).length,
  }
}

// Función para precargar imágenes de blog
export function preloadBlogImages(blogIds: string[]) {
  const preloadedImages = blogIds.map((id) => ({
    id,
    url: getBlogImageUrl(id),
    preloaded: true,
  }))

  return Promise.resolve(preloadedImages)
}

// Función adicional para obtener todas las URLs de imágenes
export function getAllImageUrls() {
  const blogUrls = Object.entries(FIXED_BLOG_IMAGES).map(([id, url]) => ({ id, url, type: "blog" }))
  const serviceUrls = Object.entries(FIXED_SERVICE_IMAGES).map(([id, data]) => ({
    id,
    url: data.featured,
    type: "service",
    gallery: data.gallery,
  }))

  return [...blogUrls, ...serviceUrls]
}
